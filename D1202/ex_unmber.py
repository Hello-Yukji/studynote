# 산술연산자
# * 덧셈(+), 뺄셈(-), 곱셈(*), 나눗셈(/)
# * 몫(//), 나머지(%), 제곱(**)

num1 = 10
num2 = 3
print(f'덧  셈 {num1} + {num2} = {num1 + num2}')
print(f'뺄  셈 {num1} - {num2} = {num1 - num2}')
print(f'곱  셉 {num1} * {num2} = {num1 * num2}')
print(f'나눗셈 {num1} / {num2} = {num1 / num2}')

print(f'몫     {num1} // {num2} = {num1 // num2}')
print(f'나머지 {num1} % {num2} = {num1 % num2}')
print(f'제  곱 {num1} ** {num2} = {num1 ** num2}')

# 비교연산자 : L > R => 결과 True/False > bool타입
#  >  : 크다            < : 작다
#  >= : 크거나 같다      <= : 작거나 같다
#  == : 같다            != : 같지 않다

print(f'크      다 {num1} >{num2} = {num1 + num2}')
print(f'작      다 {num1} < {num2} = {num1 - num2}')
print(f'크거나 같다 {num1} >= {num2} = {num1 * num2}')
print(f'작거나 같다 {num1} <= {num2} = {num1 / num2}')
print(f'같      다 {num1} == {num2} = {num1 // num2}')
print(f'같지  않다 {num1} != {num2} = {num1 % num2}')

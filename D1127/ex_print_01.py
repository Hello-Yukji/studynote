# print() 함수 사용하기
# 문법/규칙 : print ( 재료 )
#            print ( 재료, 재료, 재료, ..., 재료n )

# [1] 실제 데이터 여러개를 화면에 출력하기
print(2024)
print(11)
print(27)
print(2024, 11, 27)

print ("용띠해", "뱀띠해")
print (2024, "용띠해", "♥", 3.999)

# [2] 변수로 데이터를 화면에 출력하기
# 좋아하는 과일이름 메모리에 저장
fruits = "멜론"
# 좋아하는 계절이름 메모리에 저장
season = "가을"
# 오늘의 온도를 메모리에 저장
temp = "5"

print(fruits, season, temp)

# [3] 실제 데이터와 변수명 혼합해서 출력
print("나는", fruits, "좋아합니다.")
print("내가 좋아하는 계절", season)